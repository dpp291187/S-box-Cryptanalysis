# ---------- Utilities ----------
PARITY8 = [bin(i).count("1") & 1 for i in range(256)]

def dot8(u: int, v: int) -> int:
    """Binary dot product over F2 for 8-bit integers."""
    return PARITY8[u & v]

def component_truth_vectors(S: List[int]) -> List[int]:
    """
    Build 255 component functions f_λ as 256-bit integers (bit x = f_λ(x)).
    λ = 1..255; x = 0..255.
    """
    comps = []
    for lam in range(1, 256):
        bits = 0
        for x in range(256):
            bits |= (dot8(lam, S[x]) & 1) << x
        comps.append(bits)
    return comps

def truth_to_list(bits: int, size: int = 256) -> List[int]:
    """Convert 256-bit integer to list of 0/1 (LSB corresponds to x=0)."""
    return [(bits >> i) & 1 for i in range(size)]

# ---------- Fast Walsh-Hadamard (FWHT) ----------
def fwht_pm1(a: List[int]) -> List[int]:
    """
    FWHT on ±1 array; returns the Walsh spectrum values for (-1)^f.
    """
    a = a[:]  # copy
    n = len(a)
    h = 1
    while h < n:
        for i in range(0, n, h*2):
            j = i
            k = i + h
            for _ in range(h):
                x = a[j]; y = a[k]
                a[j] = x + y
                a[k] = x - y
                j += 1; k += 1
        h <<= 1
    return a

def walsh_hadamard(vals01: List[int]) -> List[int]:
    """Walsh spectrum W_f(u) from {0,1} truth via FWHT on (-1)^f."""
    return fwht_pm1([1 - 2*v for v in vals01])

def nonlinearity_from_walsh(W: List[int], n: int = 8) -> int:
    """NL = 2^(n-1) - max_u |W_f(u)|/2."""
    return (1 << (n-1)) - (max(abs(w) for w in W) // 2)

# ---------- Algebraic degree via Möbius ----------
def algebraic_degree_from_truth(bits: int, n: int = 8) -> int:
    """
    Compute algebraic degree via Möbius transform on the cube.
    Input as 256-bit int (truth table).
    """
    N = 1 << n
    a = [(bits >> i) & 1 for i in range(N)]
    for k in range(n):
        step = 1 << k
        for i in range(N):
            if i & step:
                a[i] ^= a[i ^ step]
    deg = 0
    for i, coef in enumerate(a):
        if coef:
            wt = (i & 0xFF).bit_count()
            if wt > deg:
                deg = wt
    return deg

# ---------- Optional: fast autocorrelation via Walsh (no O(256^2)) ----------
def ac_abs_hist_from_walsh(W: List[int]) -> Tuple[Tuple[int,int], ...]:
    """
    Compute |AC| histogram using the identity:
      AC_f = (FWHT of W_f^2) / 2^n
    We only need absolute values and a histogram for invariants.
    """
    n = 8
    N = 1 << n
    sq = [w*w for w in W]
    AC = fwht_pm1(sq)
    # divide by N (FWHT is its own inverse up to scaling)
    AC = [v // N for v in AC]
    # absolute value histogram
    from collections import Counter
    return tuple(sorted(Counter(abs(v) for v in AC).items()))

